/**
 * Adds two number and returns their sum.
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
export function add (a, b) {
  return a + b
}

/**
 * Multiplies two numbers.
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
export function multiply (a, b) {
  return a * b
}


